function build_asn_public(asnPrivateArray)
{
    var key = [];

    key.push(new Uint8Array([0x30, 0x82, 0x01, 0x22]));   // SEQUENCE
    key.push(new Uint8Array([0x30, 0x0d]));               //   SEQUENCE
    key.push(oid2Array[0]);                               //      OID
    key.push(new Uint8Array([0x05, 0x00]));               //      NULL
    key = key.concat(
        asn1_encode_bitstring(
            asn1_encode(0x30, [].concat(
                asn1_encode_integer([rsaKey["n"]]),
                asn1_encode_integer([rsaKey["e"]])))));

    return asn1_to_fields(asn1_to_uint8(key));
}

function build_asn_private(keyMap)
{
    // SEQUENCE {
    //    version:0,
    //    algorithm: SEQUENCE {
    //       OID,
    //       parameters
    //    }
    //    OCTET STRING {
    //       SEQUENCE {
    //          version: 0,
    //          n: INTEGER,
    //          e: INTEGER,
    //          d: INTEGER,
    //          p: INTEGER,
    //          q: INTEGER,
    //          dp: INTEGER,
    //          dq: INTEGER,
    //          qi: INTEGER
    //       }
    //    }
    //  }

    var x = asn1_encode(0x30, [].concat(
        asn1_encode_integer([new Uint8Array([0])]),
        asn1_encode_integer([keyMap["n"]]),
        asn1_encode_integer([keyMap["e"]]),
        asn1_encode_integer([keyMap["d"]]),
        asn1_encode_integer([keyMap["p"]]),
        asn1_encode_integer([keyMap["q"]]),
        asn1_encode_integer([keyMap["dp"]]),
        asn1_encode_integer([keyMap["dq"]]),
        asn1_encode_integer([keyMap["qi"]])));
    x = asn1_encode(0x4, x);
    var algorithmID = asn1_encode(0x30, [].concat(
        oid2Array[0], new Uint8Array([0x5, 0x0])));
    x = asn1_encode(0x30, [].concat(
        asn1_encode_integer([new Uint8Array([0])]),
        algorithmID,
        x));
    return asn1_to_fields(asn1_to_uint8(x));
}

function import_test(t, format, keyData, algorithm, extractable, keyUsages) {
    self.crypto.subtle.importKey(format, keyData, algorithm, extractable, keyUsages).then(
        t.step_func(function(newKey) {
            //
            //  Verify public fields match with what we imported
            //

            assert_equals(newKey.algorithm.name, algorithm.name);
            assert_equals(newKey.algorithm.modulusLength, 2048);
            assert_array_equals(newKey.algorithm.publicExponent, new Uint8Array([1, 0, 1]));
            assert_equals(newKey.algorithm.hash.name, algorithm.hash);

            if (format == "pkcs8") assert_equals(newKey.type, "private");
            else if (format == "spki") assert_equals(newKey.type, "public");
            else if (format == "jwk") {
                if ('d' in keyData) assert_equals(newKey.type, "private");
                else assert_equals(newKey.type, "public");
            }

            assert_equals(newKey.extractable, extractable);
            assert_array_equals(newKey.usages, keyUsages);

            //  Try and export the key now

            self.crypto.subtle.exportKey(format, newKey).then(
                t.step_func(function(data) {
                    if (!extractable) assert_unreached("Export should have failed");
                    t.done();
                }),
                t.step_func(function(err) {
                    if (extractable) assert_unreached("Export should have succeeded err=" + err);
                    t.done();
                })
            );
        }),
        t.step_func(function(err) {
            assert_unreached("Import failed with error " + err);
            t.done();
        })
    );
}

